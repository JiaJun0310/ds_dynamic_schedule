//gets the new data of the course and updates the program json file
app.post("/updateCourse", async (req, res) => {
    try {
        const {
            title,
            daysOfWeek,
            startTime,
            endTime,
            lectureHall,
            semester,
            professor
        } = req.body;

        const mergedPath = path.join(__dirname, 'jsonData', 'merged_schedule.json');

        const data = JSON.parse(fs.readFileSync(mergedPath, 'utf8'));

        //find the specific course 
        const courseIndex = data.findIndex(course => course.title === title);

        if (courseIndex === -1) {
            return res.status(404).json({ message: "Course not found" });
        }

        //replace course data 
        data[courseIndex] = {
            ...data[courseIndex], 
            title,
            daysOfWeek,
            startTime,
            endTime,
            lectureHall,
            semester,
            professor
        };

        fs.writeFileSync(mergedPath, JSON.stringify(data, null, 2), 'utf8');

        res.json({ message: "Course updated successfully!" });

    } catch (err) {
        console.error(err);
        res.status(500).json({ message: err.message });
    }
});

