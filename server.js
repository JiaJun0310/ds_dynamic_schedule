// Gets all exams for a specific semester
app.post("/getSemesterExams", async (req, res) => {
    try {
        const { semester } = req.body; 
        const examPath = path.join(__dirname, 'jsonData', 'merged_exams.json');

        // Read and parse the exam JSON file
        const data = JSON.parse(fs.readFileSync(examPath, 'utf8'));

        // Filter out only the exams that match the requested semester
        // We use String() to safely handle numbers vs strings (e.g., 7 vs "7")
        const semesterExams = data.filter(exam => String(exam.semester) === String(semester));

        // Send the filtered array directly back to the frontend
        return res.status(200).json(semesterExams); 
        
    } catch (err) {
        console.error("Error fetching semester exams:", err);
        res.status(400).json({ error: err.message });
    }
});

app.post("/getSemesterOfExams", async (req, res) => {
    try {
        const semesterPath = path.join(__dirname, 'jsonData', 'semester_exams.json');
        const makeUpPath = path.join(__dirname, 'jsonData', 'make_up_exams.json');

        // 1. Check normal semester exams first
        if (fs.existsSync(semesterPath)) {
            const exams = JSON.parse(fs.readFileSync(semesterPath, 'utf8'));
            
            // Check if valid data exists
            if (Array.isArray(exams) && exams.length > 0 && exams[0].semester) {
                const isWinter = (exams[0].semester % 2 !== 0); 
                return res.status(200).json(isWinter);
            }
        }

        //If normal exams don't exist or are empty, check make-up exams
        if (fs.existsSync(makeUpPath)) {
            const makeUpExams = JSON.parse(fs.readFileSync(makeUpPath, 'utf8'));
            
            // Check if valid data exists
            if (Array.isArray(makeUpExams) && makeUpExams.length > 0 && makeUpExams[0].semester) {
                const isWinter = (makeUpExams[0].semester % 2 === 0); 
                return res.status(200).json(isWinter);
            }
        }

        //If neither file exists or both are empty
        return res.status(404).json({ error: "No valid exams found in either file" });

    } catch (error) {
        console.error("Error fetching exams semester:", error);
        res.status(500).json({ error: "Internal server error" });
    }
});

